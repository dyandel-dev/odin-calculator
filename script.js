function addNumber(first_num, second_num) {
    return first_num + second_num;
}

function subtractNumber(first_num, second_num) {
    return first_num - second_num;
}

function multiplyNumber(first_num, second_num) {
    return first_num * second_num;
}

function divideNumber(first_num, second_num) {
    return first_num / second_num;
}